function binaryAdd(a, b) {
  var apples = parseInt(a, 2)
  var bananas = parseInt(b, 2)
  //console.log(apples + bananas)
  var basket = apples + bananas
  return basket.toString(2)
  console.log(basket.toString(2))
}


function binarySubtract(a, b) {
  var apples = parseInt(a, 2)
  var bananas = parseInt(b, 2)
  //console.log(apples + bananas)
  var basket = apples - bananas
  return basket.toString(2)
  console.log(basket.toString(2))
}
